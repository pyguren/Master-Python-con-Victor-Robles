mi_texto = "Master"
mi_texto2 = "En Python"

# Se concatena
texto_unido = mi_texto + " " + mi_texto2
print(texto_unido)

#Se produce un salto de linea
texto_unido = mi_texto + " \n " + mi_texto2
print(texto_unido)

# Se produce una tabulacion
texto_unido = mi_texto + " \t " + mi_texto2
print(texto_unido)

# Se borra lo que esta antes del \r
texto_unido = mi_texto + " \r " + mi_texto2
print(texto_unido)

